public class SortingUtils {

    public int returnMaxOfTow(int f, int s) {

        if(f > s) {
            return f;
        }
        else {
            return s;
        }

    }

    public float returnMaxOfTow(float f, float s) {

        if(f > s) {
            return f;
        }
        else {
            return s;
        }

    }

    public double returnMaxOfTow(double f, double s) {

        if(f > s) {
            return f;
        }
        else {
            return s;
        }

    }

    public int returnMinOfTow(int f, int s) {

        if(f < s) {
            return f;
        }
        else {
            return s;
        }

    }

    public float returnMinOfTow(float f, float s) {

        if(f < s) {
            return f;
        }
        else {
            return s;
        }

    }

    public double returnMinOfTow(double f, double s) {

        if(f < s) {
            return f;
        }
        else {
            return s;
        }

    }


}
