        if (autoFitTextureView.isAvailable()) {
            openCamera(autoFitTextureView.getWidth(), autoFitTextureView.getHeight());
        }
        else {
            autoFitTextureView.setSurfaceTextureListener(mSurfaceTextureListener);
        }
